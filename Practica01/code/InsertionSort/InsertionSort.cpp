#include <iostream>
#include <vector>
using namespace std;

void insertionSort(int a[],int b)
{
    int n = b;
    for (int i = 1; i < n; i++)
    {
        int key = a[i];
        int j = i - 1;
        while (j >= 0 && a[j] > key)
        {
            a[j + 1] = a[j];
            j--;
        }
        a[j + 1] = key;
    }
}